import { execFileSync } from "node:child_process";
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";

const repoRoot = resolve(import.meta.dirname, "..");
const tsc = join(repoRoot, "node_modules/.bin/tsc");
const deferredPath = join(repoRoot, "features/2026-07-02-complex/deferred.md");
const diagnosticsPath = join(
    repoRoot,
    "features/2026-07-02-complex/runtime-module-diagnostics-deferred.txt",
);

const complexityDiagnosticPattern = /error TS(?:2589|2590|2859|2321):/;
const complexityCodes = ["TS2589", "TS2590", "TS2859", "TS2321"];
const runtimeDeferredRowPattern = /^\|\s+`([^`]+)`\s+\|\s+`([^`]+)`\s+\|\s+`(\d+)`\s+\|\s+`([^`]+)`\s+\|\s+(.+?)\s+\|$/gm;
const updateInventory = process.env.UPDATE_RUNTIME_DIAGNOSTICS === "1";

const inventoryHeader = [
    "# Deferred Runtime Source Diagnostics",
    "",
    "Generated by `UPDATE_RUNTIME_DIAGNOSTICS=1 just check-runtime` from plain `tsc -p modules/runtime/tsconfig.check.json --noEmit --pretty false`.",
    "This file is the exact accepted runtime source diagnostics inventory. Ordinary `just check-runtime` runs compare against it and fail on any addition, removal, or changed diagnostic.",
    "Complexity-class diagnostics (TS2589, TS2590, TS2859, TS2321) in this file are tracked runtime source-check debt.",
    "",
];

function runRuntimeCheck() {
    try {
        execFileSync(
            tsc,
            ["-p", "modules/runtime/tsconfig.check.json", "--noEmit", "--pretty", "false"],
            {
                cwd: repoRoot,
                encoding: "utf8",
                stdio: "pipe",
                maxBuffer: 200 * 1024 * 1024,
            },
        );

        return "";
    }
    catch (error) {
        return [error.stdout, error.stderr].filter(Boolean).join("");
    }
}

function diagnosticLines(output) {
    return output
        .split("\n")
        .map(line => line.trimEnd())
        .filter(line => line.includes(": error TS"));
}

function countComplexityDiagnostics(diagnostics) {
    return Object.fromEntries(
        complexityCodes.map(code => [
            code,
            diagnostics.filter(line => line.includes(`error ${code}:`)).length,
        ]),
    );
}

function diagnosticFile(line) {
    return line.match(/^(.*)\(\d+,\d+\): error TS\d+:/)?.[1];
}

function diagnosticCode(line) {
    return line.match(/: error (TS\d+):/)?.[1];
}

function codeSummary(codes) {
    return [...codes.entries()]
        .sort((a, b) => a[0].localeCompare(b[0]))
        .map(([code, count]) => `${code}:${count}`)
        .join(", ");
}

function runtimeDeferredEntries(diagnostics) {
    const entries = new Map();

    for (const line of diagnostics) {
        const file = diagnosticFile(line);
        const code = diagnosticCode(line);

        if (!file || !code) {
            continue;
        }

        const entry = entries.get(file) ?? {
            total: 0,
            codes: new Map(),
        };

        entry.total += 1;
        entry.codes.set(code, (entry.codes.get(code) ?? 0) + 1);
        entries.set(file, entry);
    }

    return entries;
}

function readDocumentedRuntimeDeferredEntries() {
    if (!existsSync(deferredPath)) {
        throw new Error(`Missing deferred diagnostics documentation at ${deferredPath}`);
    }

    const deferred = readFileSync(deferredPath, "utf8");
    const documented = new Map();
    let match;

    while ((match = runtimeDeferredRowPattern.exec(deferred))) {
        documented.set(match[1], {
            symbol: match[2],
            total: Number(match[3]),
            codes: match[4],
            justification: match[5].trim(),
        });
    }

    return documented;
}

function compareRuntimeDeferredDocumentation(diagnostics) {
    const expected = runtimeDeferredEntries(diagnostics);
    const documented = readDocumentedRuntimeDeferredEntries();
    const failures = [];

    for (const [file, entry] of expected) {
        const row = documented.get(file);
        const expectedCodes = codeSummary(entry.codes);

        if (!row) {
            failures.push(`${file} has ${entry.total} deferred runtime diagnostics but is not documented in ${deferredPath}`);
            continue;
        }

        if (row.total !== entry.total) {
            failures.push(`${file} documents ${row.total} deferred runtime diagnostics but inventory has ${entry.total}`);
        }

        if (row.codes !== expectedCodes) {
            failures.push(`${file} documents runtime diagnostic codes '${row.codes}' but inventory has '${expectedCodes}'`);
        }

        if (row.justification.length < 40) {
            failures.push(`${file} runtime deferral justification is too short for review`);
        }
    }

    for (const file of documented.keys()) {
        if (!expected.has(file)) {
            failures.push(`${file} is documented in ${deferredPath} but has no deferred runtime diagnostics`);
        }
    }

    return failures;
}

function countLines(lines) {
    const counts = new Map();

    for (const line of lines) {
        counts.set(line, (counts.get(line) ?? 0) + 1);
    }

    return counts;
}

function compareInventories(expected, actual) {
    const actualCounts = countLines(actual);
    const expectedCounts = countLines(expected);
    const added = [];
    const removed = [];

    for (const [line, actualCount] of actualCounts) {
        const expectedCount = expectedCounts.get(line) ?? 0;
        for (let i = expectedCount; i < actualCount; i += 1) {
            added.push(line);
        }
    }

    for (const [line, expectedCount] of expectedCounts) {
        const actualCount = actualCounts.get(line) ?? 0;
        for (let i = actualCount; i < expectedCount; i += 1) {
            removed.push(line);
        }
    }

    return { added, removed };
}

function readExpectedDiagnostics() {
    if (!existsSync(diagnosticsPath)) {
        throw new Error(`Missing runtime diagnostics inventory at ${diagnosticsPath}`);
    }

    return diagnosticLines(readFileSync(diagnosticsPath, "utf8"));
}

function writeInventory(diagnostics) {
    mkdirSync(dirname(diagnosticsPath), { recursive: true });
    writeFileSync(
        diagnosticsPath,
        [
            ...inventoryHeader,
            ...diagnostics,
            "",
        ].join("\n"),
    );
}

function emitSummary(status, diagnostics) {
    const complexityDiagnostics = countComplexityDiagnostics(diagnostics);
    const complexityTotal = Object.values(complexityDiagnostics).reduce((sum, count) => sum + count, 0);

    console.log(`RUNTIME_SOURCE_DIAGNOSTICS ${JSON.stringify({
        status,
        total: diagnostics.length,
        complexityDiagnostics,
        complexityTotal,
        inventory: diagnosticsPath,
    })}`);
}

const output = runRuntimeCheck();
const diagnostics = diagnosticLines(output);
const complexityDiagnostics = diagnostics.filter(line => complexityDiagnosticPattern.test(line));

if (updateInventory) {
    writeInventory(diagnostics);
    emitSummary("inventory-updated", diagnostics);

    if (diagnostics.length > 0) {
        console.error(`Runtime source diagnostics inventory updated with ${diagnostics.length} diagnostics.`);
        console.error(`Tracked runtime complexity diagnostics: ${complexityDiagnostics.length}.`);
    }
}
else {
    const expectedDiagnostics = readExpectedDiagnostics();
    const inventoryDiff = compareInventories(expectedDiagnostics, diagnostics);
    const matchesInventory = inventoryDiff.added.length === 0 && inventoryDiff.removed.length === 0;
    const documentationDiff = matchesInventory
        ? compareRuntimeDeferredDocumentation(diagnostics)
        : [];

    emitSummary(
        matchesInventory
            ? documentationDiff.length === 0
                ? "matched-deferred-inventory"
                : "documentation-mismatch"
            : "inventory-mismatch",
        diagnostics,
    );

    if (!matchesInventory) {
        console.error(`Runtime source diagnostics do not match ${diagnosticsPath}.`);

        for (const line of inventoryDiff.added.slice(0, 20)) {
            console.error(`unexpected runtime diagnostic: ${line}`);
        }

        for (const line of inventoryDiff.removed.slice(0, 20)) {
            console.error(`missing deferred runtime diagnostic: ${line}`);
        }

        if (inventoryDiff.added.length > 20 || inventoryDiff.removed.length > 20) {
            console.error(`additional differences: ${Math.max(0, inventoryDiff.added.length - 20)} unexpected, ${Math.max(0, inventoryDiff.removed.length - 20)} missing`);
        }

        process.exit(1);
    }

    if (documentationDiff.length > 0) {
        console.error(`Runtime source diagnostics are not fully documented in ${deferredPath}.`);

        for (const line of documentationDiff.slice(0, 20)) {
            console.error(`runtime deferred documentation mismatch: ${line}`);
        }

        if (documentationDiff.length > 20) {
            console.error(`additional documentation mismatches: ${documentationDiff.length - 20}`);
        }

        process.exit(1);
    }

    if (diagnostics.length > 0) {
        console.error(`Runtime source check matched ${diagnostics.length} deferred diagnostics.`);
        console.error(`Tracked runtime complexity diagnostics: ${complexityDiagnostics.length}.`);
        console.error(`Deferred inventory: ${diagnosticsPath}`);
    }
}
